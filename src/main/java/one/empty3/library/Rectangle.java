package one.empty3.library;

public class Rectangle {
    private final int xmin;
    private final int ymin;
    private final int xmax;
    private final int ymax;

    public Rectangle(int minx, int miny, int maxx, int maxy) {
        this.xmin = minx;
        this.ymin = miny;
        this.xmax = maxx;
        this.ymax = maxy;

    }


}
