package one.empty3.library1.tree;



import one.empty3.library.StructureMatrix;
import one.empty3.library1.tree.TreeNodeType;
import org.jetbrains.annotations.NotNull;

/*__
 * Created by manuel on 16-12-16.
 */
public class SignTreeNodeType extends TreeNodeType {
    protected double sign = 1;

    public SignTreeNodeType(double d) {
        sign = d;
        sign1 = d;
    }

    public double getSign() {
        return sign;
    }

    public void setSign(double sign) {
        this.sign = sign;
        this.sign1 = sign;
    }

    @Override
    public StructureMatrix<Double> eval() {
        return new StructureMatrix<Double>(0, Double.class).setElem(sign);
    }

    @NotNull
    public String toString() {
        String string = super.toString();
        string += "\nSign sign : " + sign;
        return string;
    }
}
